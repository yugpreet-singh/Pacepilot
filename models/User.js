const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    minlength: 3,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Simple password comparison for demo (no encryption)
userSchema.methods.comparePassword = async function (candidatePassword) {
  return this.password === candidatePassword;
};

module.exports = mongoose.model("User", userSchema);
